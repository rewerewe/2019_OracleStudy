-- SYS 등 관리자를 생성할 권한을 가진 계정으로 접속해야 관리자 계정인 SIST_A001를 만들 수 있다.
-- 1. 관리자
-- 테이블 생성
CREATE USER SIST_A001 IDENTIFIED BY java006;
-- 권한 부여
GRANT CONNECT, RESOURCE, DBA TO SIST_A001;


-- SIST_A001로 접속.
-- 2. 교수
-- 테이블 생성
-- 김호진
CREATE USER SIST_B001 IDENTIFIED BY 1234567;
-- 홍길동
CREATE USER SIST_B002 IDENTIFIED BY 1234567;
-- 권한 부여
GRANT CONNECT TO SIST_B001;
GRANT CONNECT TO SIST_B002;


